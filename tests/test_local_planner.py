def test_local_planner_builder_returns_structured_proposal_with_rule_id() -> None:
    match = _build_match("demo_pwd", "pwd", {}, frozenset())

    assert match is not None
    assert match.rule_id == "demo_pwd"
    assert match.proposal.mode == "structured"
    assert match.proposal.command_family == "pwd"
    assert match.proposal.arguments == {}
    assert match.proposal.needs_confirmation is True
    assert "Generated by deterministic shortcut." in match.proposal.notes
    assert "demo_pwd" in match.proposal.explanation


