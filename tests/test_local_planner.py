def test_local_planner_rejects_unsafe_fragments() -> None:
    unsafe_values = (
        "README.md; whoami",
        "README.md && whoami",
        "README.md || whoami",
        "README.md | wc",
        "README.md > out",
        "README.md < in",
        "$(pwd)",
        "`pwd`",
        "README.md\x00",
        "README.md\nnext",
        "README.md\rnext",
    )

    for value in unsafe_values:
        assert _has_unsafe_fragment(value), value


